                    2.1.5.	Desafios esperados<br />
                    O lançamento prático da CC envolve diversos desafios, de diversas naturezas. E nesta segunda parte do manual, definiremos e justificaremos as regras, estratégias e métodos que nos ajudarão a atingir nossos objetivos e fazer dessa empreitada um grande sucesso.
                    <br />
                    Para facilitar na compreensão dos porquês das regras de funcionamento que serão apresentadas a seguir, é importante que compreendamos os principais principais desafios que enfrentaremos:
                    <br /><br />
                    2.1.5.1.	Engajamento das pessoas<br />

                    Engajamento está diretamente relacionado ao prazer em estar colaborando. As pessoas sentirão essa satisfação ao acreditarem no projeto e em sua visão ou participarem de forma ativa e construtiva das atividades. Assim, seja pela contribuição financeira, por trabalho intelectual ou físico, por participação de decisões, é necessário que as pessoas se mantenham engajadas. Assim, uma comunicação aberta e positiva, relacionamentos sadios entre todos e clareza no que está sendo feito é fundamental.
                    <br />
                    2.1.5.2.	Crescimento acelerado<br />
                    <br />Para atingirmos o impacto social almejado, a CC necessita atingir o maior número possível de pessoas. Da mesma forma, caso a iniciativa não atinja massa crítica suficiente para sustentar uma operação de resultados, poderá desestimular os membros. Assim, mecanismos de viralização online e offline devem ser utilizados de modo que o crescimento exponencial seja alcançado.
                    <br /><br /> 2.1.5.3.	 Pessoas se sentindo inseguras ou ameaçadas
                    <br />Todo processo de inovação gera insegurança às pessoas, sobre sua legitimidade, viabilidade e honestidade. Por isso todos os processos da Crowdcapital estão sendo detalhadamente descritos e estarão abertos para audição pública em sua operação.
                    <br />
                    <br />Por outro lado, ao atuar em questões econômicas como estruturas de propriedade, e refletir sobre a própria propriedade privada dos meios de produção, é possível que haja imposições ideológicas, seja por pontos de vistas técnicos, seja na defesa de interesses pessoais. Por isso, a CC, mantém quatro pilares básicos de legitimação:
                    <br />
                    <br /> Legalidade, legitimidade, experimentalidade e harmonia. Resumidamente, a CC atende à todos os requisitos legais para operar, possui uma visão legítima de criação de valor à sociedade e todos os membros tem consciência do seu caráter inovador implícito de riscos e imprevisibilidades. Além disso, a CC tem ciência de que impactos podem ser gerados ao empresariado tradicional e buscará formas de integrá-los aos processos sempre que possível, desde que haja boa vontade e participação positiva dos mesmo. Um exemplo seria, ao invés de criar um negócio tradicional do zero, a CC considerar antes comprar negócios já existentes no mercado, remunerando o ativo construído pelo empresário.
                    <br /><br /> 2.1.5.4.	Legalidade jurídica

                    <br /> A instituição cooperativa é o instrumento formal adotado pela CC para realizar sua função social. O cooperativismo é uma prática muito bem fundamentada e estabelecida no Brasil e em outros países. Porém seu uso como um centralizador de investimento e pulverizador de riscos visando coletivização de ativos produtivos é uma prática inovadora, por isso devemos buscar sempre a formação de embasamento acadêmico, jurisprudência e apoio governamental.

                    <br /><br />  2.1.5.5.	Regras de funcionamento simples, eficientes, justas e estimulantes

                    <br /> Ao elaborar uma nova forma de relação econômica entre pessoas, especialmente em um formato pulverizado, escalável, digital e equalizado, é importante que as pessoas se mantenham engajadas e interessadas. E isso só é possível através de um funcionamento simples, onde qualquer pessoa possa compreender como funciona e exercer seu papel. Além disso os processos de decisão e gestão precisam ser eficientes para garantir a rentabilidade e benefícios almejados. E ao fundir igualdade a meritocracia, é necessário que todas as regras sejam tidas como justas e legítimas pelos membros. Assim como uma experiência de uso estimulante para que a satisfação dos membros seja sempre a máxima possível.

                    <br /><br />  2.1.5.6.	Sistema tecnológico acessível
